export function getAnswerComparePrompt(userAnswer: string, question: string): string {
  return `당신은 학습 평가 전문가입니다. 학생의 답변을 분석하고 모범답안과 비교해주세요.

## 질문
${question}

## 학생의 답변
${userAnswer}

## 요청사항
1. 이 질문에 대한 이상적인 모범답안을 작성하세요.
2. 학생의 답변과 모범답안을 비교 분석하세요.
3. 학생의 답변에서 빠진 핵심 포인트를 구체적으로 나열하세요.
4. 종합 피드백을 제공하세요.

## 응답 형식
반드시 다음 JSON 형식으로만 응답하세요 (다른 텍스트 없이):

\`\`\`json
{
  "modelAnswer": "모범답안 전체 내용 (상세하게)",
  "gaps": ["빠진 핵심 포인트 1", "빠진 핵심 포인트 2", "..."],
  "feedback": "종합 피드백 (강점과 개선점 모두 포함)"
}
\`\`\`

## 중요 규칙
- 모든 내용은 한국어로 작성
- 모범답안은 충분히 상세하게 작성
- gaps는 구체적이고 실행 가능한 포인트로 작성
- 학생의 답변이 좋은 경우에도 보완할 수 있는 점을 제시`;
}
